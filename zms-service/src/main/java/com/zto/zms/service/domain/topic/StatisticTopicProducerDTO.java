package com.zto.zms.service.domain.topic;

/**
 * Created by liangyong on 2018/10/12.
 */
public class StatisticTopicProducerDTO {

    private long timestamp;
    private String clientName;
    private String ip;
    private String name;
    private double value;

    public StatisticTopicProducerDTO(long timestamp, String clientName, String ip, String name, double value) {
        this.timestamp = timestamp;
        this.clientName = clientName;
        this.ip = ip;
        this.name = name;
        this.value = value;
    }

    public long getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(long timestamp) {
        this.timestamp = timestamp;
    }

    public String getClientName() {
        return clientName;
    }

    public void setClientName(String clientName) {
        this.clientName = clientName;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getValue() {
        return value;
    }

    public void setValue(double value) {
        this.value = value;
    }
}

